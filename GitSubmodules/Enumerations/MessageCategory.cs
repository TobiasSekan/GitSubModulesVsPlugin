namespace GitSubmodules.Enumerations
{
    /// <summary>
    /// Category for a message on the output console
    /// </summary>
    internal enum Category
    {
        /// <summary>
        /// No message only a empty line
        /// </summary>
        EmptyLine,

        /// <summary>
        /// Message is a debug information
        /// </summary>
        Debug,

        /// <summary>
        /// Message is a error
        /// </summary>
        Error
    }
}
